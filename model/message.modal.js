import mongoose from "mongoose";

const messageShema = new mongoose.Schema(
  {
    sendar: {
      type: mongoose.Schema.ObjectId,
      ref: "User",
      required: true,
    },
    receiver: {
      type: mongoose.Schema.ObjectId,
      ref: "User",
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
export const Message = mongoose.model("Message", messageShema);
